export type TranslationKey = keyof typeof translations.en;

export function useTranslation() {
  const { value: settings } = useLocalStorage<Settings>(KEYS.settings, DEFAULT_SETTINGS);
  const lang = settings.lang || "en";

  const t = (key: TranslationKey, variables?: Record<string, string | number>) => {
    let str: string = translations[lang][key] || translations.en[key] || String(key);
    if (variables) {
      Object.entries(variables).forEach(([k, v]) => {
        str = str.replace(`{${k}}`, String(v));
      });
    }
    return str;
  };

  return { t, lang, isRtl: lang === "ar" };
}
